package com.shalj.wanandroid.data.mappers

import com.shalj.wanandroid.data.local.ArticleEntity
import com.shalj.wanandroid.domain.ArticleData
import com.shalj.wanandroid.net.toJson
import com.shalj.wanandroid.net.toModelList

fun ArticleData.toArticleEntity() = ArticleEntity(
    id = id,
    apkLink = apkLink,
    audit = audit,
    author = author,
    canEdit = canEdit,
    chapterId = chapterId,
    chapterName = chapterName,
    collect = collect,
    courseId = courseId,
    desc = desc,
    descMd = descMd,
    envelopePic = envelopePic,
    fresh = fresh,
    host = host,
    isAdminAdd = isAdminAdd,
    link = link,
    niceDate = niceDate,
    niceShareDate = niceShareDate,
    origin = origin,
    prefix = prefix,
    projectLink = projectLink,
    publishTime = publishTime,
    realSuperChapterId = realSuperChapterId,
    selfVisible = selfVisible,
    shareDate = shareDate,
    shareUser = shareUser,
    superChapterId = superChapterId,
    superChapterName = superChapterName,
    title = title,
    type = type,
    tags = tags?.toJson().orEmpty().ifEmpty { "[]" },
    userId = userId,
    visible = visible,
    zan = zan,
)

fun ArticleEntity.toArticleData() = ArticleData(
